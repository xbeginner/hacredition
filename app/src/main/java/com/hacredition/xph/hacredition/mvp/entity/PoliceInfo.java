public class PoliceInfo {

    private String nonghu;
    private String nonghuName;
    private String type;
    private String time;
    private String xianzhuang;

    public String getNonghu() {
        return nonghu;
    }

    public void setNonghu(String nonghu) {
        this.nonghu = nonghu;
    }

    public String getNonghuName() {
        return nonghuName;
    }

    public void setNonghuName(String nonghuName) {
        this.nonghuName = nonghuName;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getXianzhuang() {
        return xianzhuang;
    }

    public void setXianzhuang(String xianzhuang) {
        this.xianzhuang = xianzhuang;
    }
}
